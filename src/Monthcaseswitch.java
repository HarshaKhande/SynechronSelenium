
public class Monthcaseswitch {

	public static void main(String[] args) {
		
		// specify the month number 
		
		int month = 12;
		
		// switch statement 
		
		switch(month) {
		
		case 1: System.out.println("1 is January month");
		
		break;
		
		case 2: System.out.println("2 is Febrary month");
		
		break;
		
		case 3: System.out.println("3 is March month");

		break;
		
		default: System.out.println("Invalid number");
		
		
		
		
		
		}



	}

}
