
public class ForloopEg {

	public static void main(String[] args) {
		
		// print the numbers from 1 to 10 using for loop
		
		// increment
		
		for (int i = 1 ;  i < 10 ; i++) {
			
			System.out.println(i);
		}
		
		// decrement 
		
		for (int i = 10 ; i >=1 ; i--  ) {
			
			System.out.println(i);
			
		}


	}

}
