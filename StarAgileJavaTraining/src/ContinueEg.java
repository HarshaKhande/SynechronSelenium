
public class ContinueEg {

	public static void main(String[] args) {
		
		//continue is used when we want to skip a iteration and continue with the next iteration 
		
		for (int i =1 ; i <= 10 ; i++) {
			
			if(i==5) {
				
				continue;
				
			}
			
			System.out.println(i);
			
			
			
		}


	}

}
